async function seedProducts() {
  console.log('üå± Starting to seed 50 luxury watch products...\n');
  console.log('üìã Brands: Rolex, Omega, Patek Philippe, Tudor, Cartier, Tag Heuer\n');
  console.log('‚ö†Ô∏è  Important: You must be logged in as ADMIN to run this script!\n');
  
  const token = process.env.AUTH_TOKEN || '';
  if (!token) {
    console.error('‚ùå Error: AUTH_TOKEN environment variable not set!');
    console.error('   Please login as admin first and set AUTH_TOKEN=your-token\n');
    process.exit(1);
  }
  
  let successCount = 0;
  let errorCount = 0;

  for (let i = 0; i < luxuryWatches.length; i++) {
    const watch = luxuryWatches[i];
    try {
      await axios.post(`${API_URL}/watches`, watch, {
        headers: {
          'Authorization': `Bearer ${token}`,
          'Content-Type': 'application/json'
        }
      });
      successCount++;
      console.log(`‚úÖ [${i + 1}/50] Created: ${watch.brand} ${watch.name}`);
    } catch (error) {
      errorCount++;
      console.error(`‚ùå [${i + 1}/50] Failed: ${watch.brand} ${watch.name}`);
      if (error.response) {
        console.error(`   Error: ${error.response.status} - ${error.response.data.message || error.response.statusText}`);
      } else {
        console.error(`   Error: ${error.message}`);
      }
    }
  }

  console.log(`\nüìä Seed Summary:`);
  console.log(`   ‚úÖ Success: ${successCount}`);
  console.log(`   ‚ùå Failed: ${errorCount}`);
  console.log(`   üì¶ Total: ${luxuryWatches.length}`);
  
  console.log(`\nüè∑Ô∏è  Brands Distribution:`);
  const brandCounts = {};
  luxuryWatches.forEach(watch => {
    brandCounts[watch.brand] = (brandCounts[watch.brand] || 0) + 1;
  });
  
  Object.entries(brandCounts).forEach(([brand, count]) => {
    console.log(`   ${brand}: ${count} products`);
  });
}

